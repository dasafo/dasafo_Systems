            print(f"   ✖ {f['file']}:{f['line']} -> {f['type']}")
        has_failed = True
except Exception as e:
    print(f"⚠️ [GUARDIAN WARNING] Scanner falló: {e}")

# 2. BACKBONE VALIDATOR ENFORCEMENT
