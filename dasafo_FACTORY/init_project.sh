from pathlib import Path

# Rutas de la Factoría
PROJECT_ROOT = Path(os.getcwd())
FACTORY_ROOT = PROJECT_ROOT.parent.parent / "dasafo_FACTORY"

if not FACTORY_ROOT.exists():
